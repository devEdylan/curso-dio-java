package edu.edylan.tema.operadores;

public class Operadores {
    public static void main(String[] args) throws Exception {
        int a, b;
        a = 7;
        b = 6;
        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % b = " + (a % b));
    }
}
